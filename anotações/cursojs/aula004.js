//aula sobre números 

/*
    No JavaScript temos 4 tipos de números, os inteiros (por exemplo: 13), os reais ou 
    float (por exemplo: 83.1),
    o Not a Number (NaN) e o infinito (Infinity).
*/

console.log(-777) //inteiros
console.log(777) //inteiros
console.log(156 + 77) //inteiros

console.log(-12.5) //reais
console.log(12.5) //reais

console.log(22 * "não será um numero") //NaN
console.log(22 / "") //NaN

console.log(Infinity) //Infinity